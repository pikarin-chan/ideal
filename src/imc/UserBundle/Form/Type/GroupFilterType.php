            'data_class'        => 'imc\UserBundle\Entity\Group',
            'csrf_protection'   => false,
            'validation_groups' => array('filter'),
            'method'            => 'GET',
        ));
    }

    /**
     * {@inheritdoc}
     */
    public function getName()
    {
        return 'group_filter';
    }
}
